        new("it", "Italiano"),
    ];

    private static string _lang = "en";

    public static void SetLanguage(string? code)
    {
        _lang = Available.Any(l => l.Code == code) ? code! : "en";
    }

    public static string T(string key)
    {
        if (_langs.TryGetValue(_lang, out var d) && d.TryGetValue(key, out var v)) return v;
        if (_langs["en"].TryGetValue(key, out var fallback)) return fallback;
        return key;
    }

